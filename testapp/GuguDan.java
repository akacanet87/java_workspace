class GuguDan{

	public static void main( String [] args){

		for( int i=1 ; i<10 ; i++ ){

			System.out.println(i+"dan");
			System.out.println();
			System.out.println("////////////////////////////////////////////////////////////////////////////");
			System.out.println();

			for( int a=1 ; a<10 ; a++){

				int cpx = a*i;

				System.out.println(i+"x"+a+"="+cpx);
				
			}

			System.out.println();
			System.out.println("////////////////////////////////////////////////////////////////////////////");
			System.out.println();

		}
	
	}

}